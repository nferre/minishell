#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	while (((s1 && s1[i]) && (s2 && s2[i]))  && (s1[i] == s2[i]))
		i++;
	return (i);
}
