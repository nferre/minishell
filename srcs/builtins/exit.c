#include "minishell.h"

void	exit_all(t_token **tab)
{
	char *end;
	int	i;

	i = -1;
	end = "exit";
	while (i++ != 4)
		if (tab[0]->value[i] != end[i])
			return ;
	printf("exit\n");
	exit(0);
}
