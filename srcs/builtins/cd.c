#include "minishell.h"

int	cd(char *str)
{
	int	i;
	char *cd;

	cd = "cd";
	i = -1;
	while (++i != 2)
		if (cd[i] != str[i])
			return (0);
	chdir(str + i + 1);
	return (1);
}
